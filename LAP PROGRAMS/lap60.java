//Multidimensional java array
		
public class lap60 {

			public static void main(String args[]){  
			  
			//declaring and initializing 2D array  
		int arr[][]={{1,2,0},{5,4,6},{0,1,0}};  
			  
		//printing 2D array  
			for(int i=0;i<3;i++){  
			 for(int j=0;j<3;j++){  
		  System.out.print(arr[i][j]+" ");  
			 }  
			 System.out.println();  
			}  
			  

	}

}
